const hashMap = Buffer.alloc(256)

function fillHashMap5Bit(seed: number): void {
  let s = seed
  for (let i = 0; i < 256; i++) {
    hashMap[i] = (s >>> 3) & 0x1f
    s = (Math.imul(0x19660d, s) + 0x3c6ef35f) | 0
  }
}

function fillHashMap6Bit(seed: number): void {
  let s = seed
  for (let i = 0; i < 256; i++) {
    hashMap[i] = (s >>> 2) & 0x3f
    s = (Math.imul(0x19660d, s) + 0x3c6ef35f) | 0
  }
}

function grindArray(seed1: number, seed2: number, key: Buffer, version: number): void {
  if (version > 0x0d) {
    fillHashMap6Bit(seed2)
  } else {
    fillHashMap5Bit(seed1)
  }

  const switchCases = Buffer.alloc(64)
  PickOneOf32B(true, seed2)
  for (let i = 0; i < 32; i++) {
    switchCases[i] = PickOneOf32B(false, 0)
  }

  if (version > 0x0d) {
    PickOneOf32B(true, seed1)
    for (let i = 32; i < 64; i++) {
      switchCases[i] = PickOneOf32B(false, 0) + 32
    }
  }

  for (let i = 0; i < key.length; i++) {
    let foo = key[i]
    for (let ix = 0; ix < key.length; ix += 2) {
      const idx = switchCases[hashMap[key[ix]]]
      foo = O[idx](key[ix + 1], foo)
    }
    key[i] = foo
  }
}

function getEncMethod(version: number): number {
  if (version === 0xe) return 1
  if (version === 0xf) return 2
  if (version === 0x10) return 3
  if (version === 0x11) return 4
  return 0
}

function hvDecrypt(dest: Buffer, src: Buffer, version: number): void {
  const encMethod = getEncMethod(version)
  const key = Buffer.alloc(16)
  HvKeys.copy(key, 0, encMethod * 16, (encMethod + 1) * 16)

  const decipher = crypto.createDecipheriv('aes-128-ecb', key, null)
  decipher.setAutoPadding(false)
  const decrypted = Buffer.concat([decipher.update(src), decipher.final()])
  decrypted.copy(dest)
}

function magicNumberGenerator(seed: number, type: number): number {
  if (type === 1) {
    const inner = (Math.imul(0x19660d, seed ^ 0x5c5c5c5c) + 0x3c6ef35f) | 0
    return (Math.imul(0x19660d, inner) + 0x3c6ef35f) | 0
  } else {
    return (Math.imul(0x19660d, seed ^ 0x36363636) + 0x3c6ef35f) | 0
  }
}

function flipEndianness(val: number): number {
  return (
    ((val << 24) & 0xff000000) |
    ((val << 8) & 0x00ff0000) |
    ((val >>> 8) & 0x0000ff00) |
    ((val >>> 24) & 0x000000ff)
  )
}

function decryptBytes(
  buffer: Buffer,
  startPos: number,
  count: number,
  key: Buffer,
  initialCounter: Buffer
): void {
  let decryptedPos = startPos
  let counterLoc = decryptedPos % 16

  const counter = Buffer.alloc(16)
  const cryptedCounter = Buffer.alloc(16)

  const fixCounter = (pos: number): void => {
    initialCounter.copy(counter)
    const low = counter.readBigUInt64LE(0)
    const high = counter.readBigUInt64LE(8)
    const offset = BigInt(pos >> 4)

    const newLow = low + offset
    let newHigh = high
    if (newLow < low) {
      newHigh += 1n
    }
    counter.writeBigUInt64LE(newLow & 0xffffffffffffffffn, 0)
    counter.writeBigUInt64LE(newHigh & 0xffffffffffffffffn, 8)
  }

  fixCounter(decryptedPos)

  const cipher = crypto.createCipheriv('aes-128-ecb', key, null)
  cipher.setAutoPadding(false)
  const keystream = Buffer.concat([cipher.update(counter), cipher.final()])
  keystream.copy(cryptedCounter)

  for (let i = 0; i < count; i++) {
    if (decryptedPos !== startPos && decryptedPos % 16 === 0) {
      fixCounter(decryptedPos)
      counterLoc = 0
      const c = crypto.createCipheriv('aes-128-ecb', key, null)
      c.setAutoPadding(false)
      const ks = Buffer.concat([c.update(counter), c.final()])
      ks.copy(cryptedCounter)
    }
    buffer[i] ^= cryptedCounter[counterLoc]
    decryptedPos++
    counterLoc++
  }
}

export function decryptMoggBuffer(moggBuf: Buffer): Buffer {
  const version = moggBuf.readInt32LE(0)
  const hmxHeaderSize = moggBuf.readUInt32LE(4)

  if (version < 0xa || version > 0x11) {
    throw new Error(`Unsupported MOGG version: ${version}`)
  }

  const oggDataSize = moggBuf.length - hmxHeaderSize
  const decryptedOgg = Buffer.alloc(oggDataSize)

  if (version === 0xa) {
    moggBuf.copy(decryptedOgg, 0, hmxHeaderSize, moggBuf.length)
    return decryptedOgg
  }

  const oggMapEntries = moggBuf.readUInt32LE(16)
  const metadataOffset = 20 + 8 * oggMapEntries

  const initialCounter = Buffer.alloc(16)
  moggBuf.copy(initialCounter, 0, metadataOffset, metadataOffset + 16)

  let seed_1 = 0
  let seed_2 = 0
  let keyIndex = 0
  let v17_type = 0
  const decryptedFileKey = Buffer.alloc(16)
  const ctrKey = Buffer.alloc(16)

  if (version === 0xb) {
    ctrKey0B.copy(ctrKey)
  } else {
    const tmp1 = moggBuf.readBigUInt64LE(metadataOffset + 16)
    seed_1 = Number(tmp1 & 0xffffffffn)

    const tmp2 = moggBuf.readBigUInt64LE(metadataOffset + 24)
    seed_2 = Number(tmp2 & 0xffffffffn)

    const fileKey = Buffer.alloc(16)
    moggBuf.copy(fileKey, 0, metadataOffset + 48, metadataOffset + 64)

    hvDecrypt(decryptedFileKey, fileKey, version)

    let offsetAfterKey = metadataOffset + 64
    if (version === 0x11) {
      const tmp17 = moggBuf.readBigUInt64LE(offsetAfterKey)
      v17_type = Number(tmp17 & 0xffffffffn)
      offsetAfterKey += 8
    }

    const tmpIdx = moggBuf.readBigUInt64LE(offsetAfterKey)
    keyIndex = (Number(tmpIdx & 0xffffffffn) % 6) + 6

    const masterKey = getMasterKey()
    const tmpKey = Buffer.alloc(16)
    if (version === 0x11) {
      getKey17(keyIndex, tmpKey, masterKey, v17_type)
    } else {
      getKey(keyIndex, tmpKey, masterKey)
    }

    grindArray(seed_1, seed_2, tmpKey, version)

    for (let i = 0; i < 16; i++) {
      ctrKey[i] = tmpKey[i] ^ decryptedFileKey[i]
    }
  }

  const first32 = Buffer.alloc(32)
  moggBuf.copy(first32, 0, hmxHeaderSize, hmxHeaderSize + 32)

  decryptBytes(first32, 0, 32, ctrKey, initialCounter)

  if (first32.readUInt32LE(0) === 0x41584d48) {
    first32.writeUInt32LE(0x5367674f, 0)

    if (version > 0xb) {
      const dw3 = first32.readUInt32LE(12)
      const val3 = dw3 ^ flipEndianness(magicNumberGenerator(seed_1, 1))
      first32.writeUInt32LE(val3 >>> 0, 12)

      const dw5 = first32.readUInt32LE(20)
      const val5 = dw5 ^ flipEndianness(magicNumberGenerator(seed_2, 2))
      first32.writeUInt32LE(val5 >>> 0, 20)
    }
  }

  first32.copy(decryptedOgg, 0, 0, 32)

  const remainingSize = oggDataSize - 32
  if (remainingSize > 0) {
    const remainingSub = decryptedOgg.subarray(32, oggDataSize)
    moggBuf.copy(remainingSub, 0, hmxHeaderSize + 32, moggBuf.length)
    decryptBytes(remainingSub, 32, remainingSize, ctrKey, initialCounter)
  }

  return decryptedOgg
}
