let s_seed = 0xeb
function mcrandom(seed: number): number {
  if (seed) s_seed = seed
  s_seed = (Math.imul(s_seed, 0x19660e) + 0x3c6ef35f) | 0
  return s_seed
}

function getMasterKey(): Buffer {
  const buf = Buffer.alloc(32)
  s_seed = 0xeb
  for (let i = 0; i < 8; i++) {
    const res = mcrandom(i === 0 ? 0xeb : 0)
    buf.writeInt32LE(res, i * 4)
  }
  return buf
}

function roll(arg: number): number {
  return (arg + 19) % 32
}

function swap(buf: Buffer, i: number, j: number): void {
  const tmp = buf[i]
  buf[i] = buf[j]
  buf[j] = tmp
}

function shuffle1(buf: Buffer): void {
  for (let i = 0; i < 8; i++) {
    swap(buf, roll(i * 4), i * 4 + 2)
    swap(buf, roll(i * 4 + 3), i * 4 + 1)
  }
}

function shuffle2(buf: Buffer): void {
  for (let i = 0; i < 8; i++) {
    swap(buf, 29 - i * 4, i * 4 + 2)
    swap(buf, 28 - i * 4, i * 4 + 3)
  }
}

function shuffle3(buf: Buffer): void {
  for (let i = 0; i < 8; i++) {
    swap(buf, roll(4 * (7 - i) + 1), i * 4 + 2)
    swap(buf, 4 * (7 - i), i * 4 + 3)
  }
}

function shuffle4(buf: Buffer): void {
  for (let i = 0; i < 8; i++) {
    swap(buf, 29 - i * 4, i * 4 + 2)
    swap(buf, roll(4 * (7 - i)), i * 4 + 3)
  }
}

function shuffle5(buf: Buffer): void {
  for (let i = 0; i < 8; i++) {
    swap(buf, 29 - i * 4, roll(i * 4 + 2))
    swap(buf, 28 - i * 4, i * 4 + 3)
  }
}

function shuffle6(buf: Buffer): void {
  for (let i = 0; i < 8; i++) {
    swap(buf, 29 - i * 4, i * 4 + 2)
    swap(buf, 28 - i * 4, roll(i * 4 + 3))
  }
}

function supershuffle(buf: Buffer): void {
  shuffle1(buf)
  shuffle2(buf)
  shuffle3(buf)
  shuffle4(buf)
  shuffle5(buf)
  shuffle6(buf)
}

function mash(x1: Buffer, x2: Buffer): void {
  for (let c = 0; c < 32; c++) {
    x1[c] ^= x2[c]
  }
}

function revealKey(masterKey: Buffer, revealed: Buffer): void {
  for (let c = 14; c > 0; c--) {
    supershuffle(revealed)
  }
  mash(revealed, masterKey)
}

function asciiDigitToHex(char: number): number {
  if (char > 96) return char - 87 // a-f
  if (char > 64) return char - 55 // A-F
  if (char > 47) return char - 48 // 0-9
  return 0
}

function asciiDigitToHex17(char: number): number {
  if (char - 97 > 5) {
    if (char - 65 > 5) {
      return char - 48
    } else {
      return char - 55
    }
  }
  return char - 87
}

function parseHex16(src: Buffer, dest: Buffer): void {
  for (let count = 0; count < 16; count++) {
    const high = asciiDigitToHex(src[count * 2])
    const low = asciiDigitToHex(src[count * 2 + 1])
    dest[count] = (high << 4) | low
  }
}

function getKey(offset: number, key: Buffer, masterKey: Buffer): void {
  const revealedKey = Buffer.alloc(32)
  hiddenKeys.copy(revealedKey, 0, offset * 32, (offset + 1) * 32)
  revealKey(masterKey, revealedKey)
  parseHex16(revealedKey, key)
}

function getKey17(offset: number, key: Buffer, masterKey: Buffer, type: number): void {
  const revealedKey = Buffer.alloc(32)
  let keyTable = hiddenKeys
  if (type === 1) keyTable = hiddenKeys_RB4
  else if (type === 4) keyTable = hiddenKeys_DM
  else if (type === 8) keyTable = hiddenKeys_Audica
  else if (type === 10) keyTable = hiddenKeys_FUSER

  keyTable.copy(revealedKey, 0, offset * 32, (offset + 1) * 32)
  revealKey(masterKey, revealedKey)
  for (let count = 0; count < 16; count++) {
    const high = asciiDigitToHex17(revealedKey[count * 2])
    const low = asciiDigitToHex17(revealedKey[count * 2 + 1])
    key[count] = (high << 4) | low
  }
}

let seedA = 0
const usedUpA = Buffer.alloc(32)

function PickOneOf32A(init: boolean, seed: number): number {
  if (init) {
    seedA = seed
    usedUpA.fill(0)
    return 0x610a660f
  }
  let tmp = 0
  do {
    seedA = (Math.imul(seedA, 0x19660d) + 0x3c6ef35f) | 0
    tmp = (seedA >>> 2) & 0x1f
  } while (usedUpA[tmp] === 1)
  usedUpA[tmp] = 1
  return tmp
}

